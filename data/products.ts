// ---------- Helpers de consulta ----------

export function getProduct(slug: string): Product | undefined {
  return products.find((p) => p.slug === slug);
}

export function productsByTeam(teamSlug: string): Product[] {
  return products.filter((p) => p.teamSlug === teamSlug);
}

export function related(product: Product, limit = 4): Product[] {
  const pool = products.filter((p) => p.slug !== product.slug);
  const score = (p: Product) =>
    (p.teamSlug === product.teamSlug ? 4 : 0) +
    (p.league === product.league ? 2 : 0) +
    (p.collection === product.collection ? 1 : 0) +
    (p.season === product.season ? 1 : 0);
  return [...pool].sort((a, b) => score(b) - score(a)).slice(0, limit);
}

